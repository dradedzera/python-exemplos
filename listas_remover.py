alunos=["Adriana", "Joao", "Breno", "Gustavo", "Hariel"]
print(f"LIsta de alunos: {alunos}")
# Método remove()
alunos.remove("Adriana")
print(f"Lista alunos: {alunos}")
# Método pop(2)
alunos.pop(2)
print(f"Lista alunos: {alunos}")
# Função del
del alunos[3]
print(f"Lista alunos: {alunos}")
# Método clear()
alunos.clear()
print(f"Lista de alunos: {alunos}")